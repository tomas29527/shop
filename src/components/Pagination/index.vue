<template>

  <div class="pagination">
    <button :disabled="startNumAndEndNum.start <=1" @click="subPageNo(pageNo-1)">上一页</button>
    <button v-if="startNumAndEndNum.start>1" @click="subPageNo(1)" >1</button>
    <button v-if="startNumAndEndNum.start>2">···</button>

    <button v-for="(page,index) in showPageNum" :key="index"  @click="subPageNo(page)" :class="{active:pageNo==page}" >{{ page }} </button>

    <button v-if="startNumAndEndNum.end<totalPage-1" >···</button>
    <button @click="subPageNo(totalPage)" v-if="startNumAndEndNum.end < totalPage">{{totalPage}}</button>
    <button :disabled="startNumAndEndNum.end >=totalPage" @click="subPageNo(pageNo+1)">下一页</button>
    <button style="margin-left: 30px">共 {{ totalSize }} 条</button>
  </div>

</template>

<script>
export default {
  name: "Pagination",
  props: ['pageNo', 'pageSize', 'totalSize', 'continues'],
  methods:{
    subPageNo(pageNo){
      this.$emit('getPageNo',pageNo)
    }
  },
  computed: {
    //总共多少页
    totalPage() {
      return Math.ceil(this.totalSize / this.pageSize)
    },
    //计算开始页和结束页
    startNumAndEndNum() {
      //定义开始和介绍页
      let start, end = 0
      //连续页大于总页数的时候
      if (this.continues > this.totalPage) {
        start = 1;
        end = this.totalPage;
      } else {
        //正常的开始和结束页
        start = this.pageNo - parseInt(this.continues / 2)
        end = this.pageNo + parseInt(this.continues / 2)
        //灵界值，开始值小于1页
        if (start < 1) {
          start = 1
          end = this.continues
        }
        //灵界值，结束值大于总页数
        if (end > this.totalPage) {
          start = this.totalPage - this.continues + 1
          end = this.totalPage
        }
      }
      return {start, end}
    },
    showPageNum() {
      let loopStart = this.startNumAndEndNum.start
      let loopEnd = this.startNumAndEndNum.end
      let num=[]
      for (let i = loopStart; i <=loopEnd; i++) {
        num.push(i)
      }
      return num
    }
  }
}
</script>

